#include "stdio.h"
void main()
{
    //计算1+3+5+...+99，100以内奇数的和
    int i = 0;
    int j = 0;
    int sum = 0;
    int sum1 = 0;
    while(i < 100) { // 这个循环体执行了100次
    	if((i + 1) % 2 != 0) { //判断该数是否为奇数
    		sum += (i + 1);
    	}
    	i++;
    }
    printf("1+3+5+...+99=%d\n", sum);
    
    while(j < 100) { // 这个循环体执行了50次，并且没有判断
    	sum1 += (j + 1);
    	j += 2; // 增量表达式不一定是加1，根据实际情况设置
    }
    printf("1+3+5+...+99=%d\n", sum1);
    
}