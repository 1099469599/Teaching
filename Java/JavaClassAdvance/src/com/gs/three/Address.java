package com.gs.three;

public class Address {
	
	private String provice;
	private String city;
	
	public Address() {
		
	}

	public String getProvice() {
		return provice;
	}

	public void setProvice(String provice) {
		this.provice = provice;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}
	


}
